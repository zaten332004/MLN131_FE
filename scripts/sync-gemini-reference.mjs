import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const root = process.cwd();
const source = path.join(root, "guidelines", "gemini-priority-chuong-5.txt");
const targetDir = path.join(root, "api", "guidelines");
const target = path.join(targetDir, "gemini-priority-chuong-5.txt");
const generated = path.join(root, "api", "gemini-priority-chuong-5.generated.ts");

const content = await readFile(source, "utf8");
await mkdir(targetDir, { recursive: true });
await writeFile(target, content, "utf8");

const generatedBody = [
  "/* eslint-disable */",
  "// This file is generated by scripts/sync-gemini-reference.mjs",
  "export const CH5_REFERENCE_TEXT = " + JSON.stringify(String(content ?? "")) + ";",
  "",
].join("\n");
await writeFile(generated, generatedBody, "utf8");

console.log(
  `Synced Gemini reference: ${path.relative(root, source)} -> ${path.relative(root, target)} (+ ${path.relative(root, generated)})`,
);
